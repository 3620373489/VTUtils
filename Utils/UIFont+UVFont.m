#import "UIFont+UVFont.h"

@implementation UIFont (UVFont)

+ (UIFont *)uvFontWithSize:(CGFloat)fontSize
{
    return [UIFont fontWithName:@"HiraginoSansGB-W3" size:fontSize];
}

@end
